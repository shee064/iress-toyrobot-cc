package com.si.iress.toyrobot.trc.model;
/**
 * TableArea object to set specific Length and width
 * @author sheenai
 *
 */
public class TableArea {

	private int unitL;
	private int unitW;
	
	public TableArea(int unitL, int unitW) {
		this.unitL = unitL;
		this.unitW = unitW;
	}

	//getters
	public int getUnitL() {
		return unitL;
	}



	public int getUnitW() {
		return unitW;
	}

	
	
}
